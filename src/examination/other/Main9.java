    Map<Character, Integer> ori = new HashMap<>();


    public String minWindow(String s, String t) {
        int left = 0, right = s.length();
        for (int i = 0; i < t.length(); i++) {
            ori.put(t.charAt(i), ori.getOrDefault(t.charAt(i), 0) + 1);
        }
        for (int i = 0; i < s.length(); i++) {
            Map<Character, Integer> cnt = new HashMap<>();
            for (int j = i; j < s.length(); j++) {
                cnt.put(s.charAt(j), cnt.getOrDefault(s.charAt(j), 0) + 1);
                if (check(cnt)) {
                    if (j - i + 1 < right - left + 1) {
                        left = i;
                        right = j;
                    }
                    break;
                }
            }
        }
        return s.substring(left, right + 1);
    }

    public boolean check(Map<Character, Integer> cnt) {
        Iterator<Map.Entry<Character, Integer>> iterator = ori.entrySet().iterator();
        while (iterator.hasNext()) {
            Map.Entry<Character, Integer> entry = iterator.next();
            Character key = entry.getKey();
            Integer value = entry.getValue();
            if (cnt.getOrDefault(key, 0) < value) {
                return false;
            }
        }
        return true;
    }
